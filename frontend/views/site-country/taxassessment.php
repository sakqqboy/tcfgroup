            <div class="col-12 title-Background">
                Background
            </div>
            <div class="col-10 Background-abbreviation">
                An inevitable fact of this world is that today or tomorrow, every person has to die at some point of time. But unlike the sudden ending of human life, tax compliance does not end all of a sudden.
                So, after a person passes away, his legal representative shall be liable to pay the final tax on behalf of that deceased person. The Income-tax Ordinance, under Section 92, the representative of that departed person shall pay the tax which the deceased would have been liable to pay if he had not died, in the like manner and to the same extent as the deceased; and the legal representative of the deceased shall, for this Ordinance, be an assessee.
            </div>
            <div class="col-12 title-Background">
                Who will be the ‘Assessee’ instead of the deceased person?
            </div>
            <div class="col-10 Background-abbreviation">
                <li> In a broad scene, a Legal representative/assessee is a person in law who represents the assets (estate) of the deceased. That person shall be responsible for all the interests of the deceased person.</li>
                <li> It can be selected by any person by his/her ‘Will’ before death or, if not decided before death then, other than exceptional cases, it's the immediate family members who will looks after his or her estate.</li>
                <li> The Deputy Commissioner of Taxes shall issue a notice to that person before deeming the legal representative of the deceased to be an assessee.</li>
                <li> The assessee shall perform all the tax-related duties which the deceased person would have performed if not dead.</li>
                Learn more about <a href="" class="no-underline3"><span class="title-Assessee"> Assessee</span></a>
            </div>
            <div class="col-12 title-Background">
                Duties of the Assessee
            </div>
            <div class="col-10 Background-abbreviation">
                <li> While making the assessment of the income of the deceased person and recovery of tax, the compliances will be followed in the same way it would have been followed if that deceased person would not have died.</li>
                <li> So, any proceeding which was allowed to take against the deceased, if he had not died, now might be taken against the legal representative as well</li>
                <li> In a nutshell, all the provisions of this Ordinance shall, so far as may be, apply accordingly.</li>
                <li> Under this Ordinance, the liability of a legal representative shall be limited to the assets' capacity which is left by the deceased person.</li>
            </div>
            <div class="col-12 title-Background">
                When the responsibility of the Assessee will start?
            </div>
            <div class="col-10 Background-abbreviation">
                <li> From the date of death of the deceased person, his/her legal representative shall start being liable for the income tax of the departed till the income year ends.</li>
                <li> In Bangladesh the income year of an individual is from July to June.</li>
                <li> When a person is deceased, his representative should clear all the tax matters to avoid any problems in the future. If the representative is unaware of the fact, after getting notice from the DCT the representative should start the procedure to submit tax return as soon as possible.</li>
                <li> If there is any unusual situation, then the Deputy Commissioner of Taxes will take necessary measures on this matter. If the legal representative does not submit the return in time, or receives the notice after the income year has been ended or even after more time has passed, there will be no penalty according to The Income-tax Ordinance, under Section 124. Upon receiving the notice from DCT, the assessee should submit the return for the last income year of that departed person.</li>
            </div>
